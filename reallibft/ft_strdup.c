#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	i;
	char	*cpy;

	i = ft_strlen(str);
	cpy = (char *)malloc(sizeof(char) * (i + 1));
	if (!cpy)
		return (NULL);
	i = 0;
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
